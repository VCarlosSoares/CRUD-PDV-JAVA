    private void buCadastrarProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buCadastrarProdActionPerformed
        Produto produto = new Produto();
        if (MenuCadProduto.executar(null, OperacaoCadastro.ocIncluir, produto)) {
            modeloProduto.incluirProduto(produtoBD.inserirProduto(produto));
        }
    }//GEN-LAST:event_buCadastrarProdActionPerformed

    private void buAlterarProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buAlterarProdActionPerformed
        int indice = tabProdutos.getSelectedRow();
        if (indice >= 0) {
            Produto produto = modeloProduto.obterProduto(indice);
            if (MenuCadProduto.executar(null, OperacaoCadastro.ocAlterar, produto)) {
                produtoBD.alterarProduto(produto);
                modeloProduto.atualizarProduto(indice, produto);
            }
        }
    }//GEN-LAST:event_buAlterarProdActionPerformed

    private void buExcluirProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buExcluirProdActionPerformed
        int indice = tabProdutos.getSelectedRow();
        if (indice >= 0) {
            Produto produto = modeloProduto.obterProduto(indice);
            int opcao = JOptionPane.showConfirmDialog(this, "Confirmar Exclusao de:  "+produto.obterNome(), "Exclusao", 
                                                      JOptionPane.YES_NO_OPTION);
            if (opcao == 0) {
                produtoBD.excluirProduto(produto);
                modeloProduto.excluirProduto(indice);
            }
        }
    }//GEN-LAST:event_buExcluirProdActionPerformed

    private void buConsultarProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buConsultarProdActionPerformed
        int indice = tabProdutos.getSelectedRow();
        if (indice >= 0) {
            Produto produto = modeloProduto.obterProduto(indice);
            MenuCadProduto.executar(null, OperacaoCadastro.ocConsultar, produto);
        }
    }//GEN-LAST:event_buConsultarProdActionPerformed

    private void buBuscarProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buBuscarProdActionPerformed
        ArrayList <Produto> produtos = new ArrayList<Produto>();
        produtos = consultarProdutos();
        if (produtos != null) {
            atualizarModelo(produtos);
        }
    }//GEN-LAST:event_buBuscarProdActionPerformed

