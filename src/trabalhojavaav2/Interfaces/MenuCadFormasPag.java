    private void buOkFormaPagActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buOkFormaPagActionPerformed
        boolean valido = true;
        try {
            if (operacaoCadastro != OperacaoCadastro.ocConsultar) {
                
                String camposInvalidos[] = new String [2];
                int index = 1;
                
                if (!textNomeFormaPag.getText().equals("")) {
                    formaPag.atualizarNome(textNomeFormaPag.getText().toUpperCase());
                } else {
                    camposInvalidos[index++] = "Nome";
                    valido = false;
                }
                
                if (valido) {
                    confirmado = true;
                } else {
                    camposInvalidos[0] = "Entrada Invalida:\n\n";
                    JOptionPane.showMessageDialog(this, camposInvalidos);
                }
                
            }
        } catch (Exception e) {
            System.err.println(e.getMessage());
            JOptionPane.showMessageDialog(this, "Operacao Invalida!\n"+e);
        }
        if (valido) {
            dispose();
        }
    }//GEN-LAST:event_buOkFormaPagActionPerformed

